Requirements
------------
 * PHP 5.2
 * MySQL 5.0.45
 * Joomla 1.5.12 with Database Type set to 'MySQLi'
   (during installation or in configuration.php: var $dbtype = 'mysqli';)
 * Nooku Framework 0.7alpha (installed automatically)

